CREATE TABLE score_snapshots (
    score_snapshot_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    target_type VARCHAR(30) NOT NULL,
    store_id BIGINT REFERENCES stores(store_id) ON DELETE RESTRICT,
    builder_store_id BIGINT REFERENCES builder_stores(builder_store_id) ON DELETE CASCADE,
    commercial_area_id BIGINT REFERENCES commercial_areas(commercial_area_id) ON DELETE RESTRICT,
    region_id BIGINT REFERENCES regions(region_id) ON DELETE RESTRICT,
    score_type VARCHAR(20) NOT NULL,
    reference_month DATE NOT NULL,
    total_score DECIMAL(5,2) NOT NULL,
    grade VARCHAR(5) NOT NULL,
    pd DECIMAL(6,2),
    survival_probability DECIMAL(6,2),
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT chk_score_target_type CHECK (target_type IN ('STORE', 'BUILDER_STORE', 'COMMERCIAL_AREA', 'REGION')),
    CONSTRAINT chk_score_type CHECK (score_type IN ('RISK', 'PROPERTY')),
    CONSTRAINT chk_score_grade CHECK (grade IN ('A', 'B', 'C', 'D', 'E')),
    CONSTRAINT chk_score_reference_month CHECK (reference_month = date_trunc('month', reference_month)::date),
    CONSTRAINT chk_score_values CHECK (
        total_score BETWEEN 0 AND 100
        AND (pd IS NULL OR pd BETWEEN 0 AND 100)
        AND (survival_probability IS NULL OR survival_probability BETWEEN 0 AND 100)
    ),
    CONSTRAINT chk_score_target_fk CHECK (
        (target_type = 'STORE' AND store_id IS NOT NULL AND builder_store_id IS NULL AND commercial_area_id IS NULL AND region_id IS NULL)
        OR (target_type = 'BUILDER_STORE' AND store_id IS NULL AND builder_store_id IS NOT NULL AND commercial_area_id IS NULL AND region_id IS NULL)
        OR (target_type = 'COMMERCIAL_AREA' AND store_id IS NULL AND builder_store_id IS NULL AND commercial_area_id IS NOT NULL AND region_id IS NULL)
        OR (target_type = 'REGION' AND store_id IS NULL AND builder_store_id IS NULL AND commercial_area_id IS NULL AND region_id IS NOT NULL)
    )
);

CREATE UNIQUE INDEX uq_score_store_month
ON score_snapshots (store_id, score_type, reference_month)
WHERE target_type = 'STORE';

CREATE UNIQUE INDEX uq_score_builder_store_month
ON score_snapshots (builder_store_id, score_type, reference_month)
WHERE target_type = 'BUILDER_STORE';

CREATE UNIQUE INDEX uq_score_commercial_area_month
ON score_snapshots (commercial_area_id, score_type, reference_month)
WHERE target_type = 'COMMERCIAL_AREA';

CREATE UNIQUE INDEX uq_score_region_month
ON score_snapshots (region_id, score_type, reference_month)
WHERE target_type = 'REGION';

CREATE TABLE score_component_snapshots (
    score_component_snapshot_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
